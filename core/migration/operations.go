// RenameField renames a column.
type RenameField struct {
	App     string
	Model   string
	Table   string
	OldName string
	NewName string
	Type    string // needed for MySQL which requires the type in CHANGE COLUMN
}

func (op *RenameField) AppLabel() string  { return op.App }
func (op *RenameField) ModelName() string { return op.Model }
func (op *RenameField) Description() string {
	return fmt.Sprintf("Rename field %s to %s on %s", op.OldName, op.NewName, op.Model)
}

func (op *RenameField) SQL(dialect string) []string {
	q := dialectQuote(dialect)
	if dialect == "mysql" {
		return []string{fmt.Sprintf("ALTER TABLE %s%s%s CHANGE COLUMN %s%s%s %s%s%s %s;",
			q, op.Table, q, q, op.OldName, q, q, op.NewName, q, op.Type)}
	}
	return []string{fmt.Sprintf("ALTER TABLE %s%s%s RENAME COLUMN %s%s%s TO %s%s%s;",
		q, op.Table, q, q, op.OldName, q, q, op.NewName, q)}
}

func (op *RenameField) RollbackSQL(dialect string) []string {
	q := dialectQuote(dialect)
	if dialect == "mysql" {
		return []string{fmt.Sprintf("ALTER TABLE %s%s%s CHANGE COLUMN %s%s%s %s%s%s %s;",
			q, op.Table, q, q, op.NewName, q, q, op.OldName, q, op.Type)}
	}
	return []string{fmt.Sprintf("ALTER TABLE %s%s%s RENAME COLUMN %s%s%s TO %s%s%s;",
		q, op.Table, q, q, op.NewName, q, q, op.OldName, q)}
}

// RunSQL lets you embed raw SQL in a migration — equivalent to Django's migrations.RunSQL.
type RunSQL struct {
	App      string
	Forward  string
	Backward string
}

func (op *RunSQL) AppLabel() string    { return op.App }
func (op *RunSQL) ModelName() string   { return "" }
func (op *RunSQL) Description() string { return "Run raw SQL" }
func (op *RunSQL) SQL(_ string) []string {
	if op.Forward == "" {
		return nil
	}
	return []string{op.Forward}
}
func (op *RunSQL) RollbackSQL(_ string) []string {
	if op.Backward == "" {
		return nil
	}
	return []string{op.Backward}
}

// ---- helpers ---------------------------------------------------------------

func dialectQuote(dialect string) string {
	if dialect == "postgres" {
		return "\""
	}
	return "`"
}

func joinLines(lines []string) string {
	out := ""
	for i, l := range lines {
		if i > 0 {
			out += ",\n"
		}
		out += l
	}
	return out
}

func fieldSQL(f FieldDef, dialect string) string {
	q := dialectQuote(dialect)
	switch f.Type {
	case "AutoField":
		if dialect == "postgres" {
			return "SERIAL PRIMARY KEY"
		}
		if dialect == "sqlite3" {
			return "INTEGER PRIMARY KEY AUTOINCREMENT"
		}
		return "INT NOT NULL AUTO_INCREMENT PRIMARY KEY"
	case "BooleanField":
		return "BOOL"
	case "CharField":
		size := f.MaxSize
		if size == 0 {
			size = 255
		}
		return fmt.Sprintf("VARCHAR(%d)", size)
	case "TextField":
		return "LONGTEXT"
	case "IntegerField":
		return "INT"
	case "SmallIntegerField":
		return "SMALLINT"
	case "BigIntegerField":
		return "BIGINT"
	case "PositiveIntegerField":
		return "INT UNSIGNED"
	case "PositiveSmallIntegerField":
		return "SMALLINT UNSIGNED"
	case "PositiveBigIntegerField":
		return "BIGINT UNSIGNED"
	case "FloatField":
		return "DOUBLE PRECISION"
	case "DecimalField":
		d, dec := f.Digits, f.Decimals
		if d == 0 {
			d = 10
		}
		return fmt.Sprintf("DECIMAL(%d,%d)", d, dec)
	case "DateField":
		return "DATE"
	case "TimeField":
		return "TIME"
	case "DateTimeField":
		return "DATETIME"
	case "JSONField":
		return "JSON"
	case "ForeignKey":
		return fmt.Sprintf("INT REFERENCES %s%s%s(id)", q, f.RelTable, q)
	case "OneToOneField":
		return fmt.Sprintf("INT UNIQUE REFERENCES %s%s%s(id)", q, f.RelTable, q)
	default:
		return "TEXT"
	}
}
